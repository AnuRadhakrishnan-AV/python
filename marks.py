# 7.program to display grades when user enter their marks.

a=float(input("enter your marks"))
if a>=90:
    print("A+ grade")
elif a>=80:
    print("A grade")
elif a>=70:
    print("B+ grade")
elif a>=60:
    print("B grade")
elif a>=50:
    print("C+ grade")
elif a>=40:
    print("C grade")
elif a>=30:
    print("D+ grade")
elif a>=20:
    print("D grade")
else:
    print("failed ")






