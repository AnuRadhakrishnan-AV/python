
# 3. print the week days on users choice

a=int(input("enter a number"))
if(a==0):
    print("sunday")
elif(a==1):
    print("monday")
elif(a==2):
    print("tuesday")
elif(a==3):
    print("wednesday")
elif(a==4):
    print("thursday")
elif(a==5):
    print("friday")
elif(a==6):
    print("saturday")
else:
    print("enter valid number")




